     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(CusAddReq $request, Customer $customer)
    {
        request()->merge(['password' => bcrypt(request()->password)]);
        $model = $customer->add();
        
        if($model){
            return redirect()->route('admincustomer.index')->with('success','Add New Successful Customers');
        }else{
            return redirect()->back()->with('error','Add New Customer failed');
        }
    }

